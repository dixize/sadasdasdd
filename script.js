function openSlide(id) {
    const s = slideData[id];
    const content = document.getElementById('modal-content');
    
    content.innerHTML = `
        <h2 class="slide-title">${s.title}</h2>
        
        <div class="info-block">
            <h4>Предпосылки развития</h4>
            <ul class="text-list">
                ${s.pre.map(p => `<li>${p}</li>`).join('')}
            </ul>
        </div>

        <div class="info-block">
            <h4>Область применения</h4>
            <ul class="text-list">
                ${s.areas.map(a => `<li><strong>${a.t}</strong>${a.d}</li>`).join('')}
            </ul>
        </div>

        <div class="info-block">
            <h4>Прогнозируемые эффекты</h4>
            <div class="effect-pill-grid">
                ${s.effects.map(e => `<div class="pill">${e}</div>`).join('')}
            </div>
        </div>

        <div class="partner-box">
            ${s.partners.map(p => `<span class="p-tag">${p}</span>`).join('')}
        </div>
    `;

    document.getElementById('modal').classList.remove('hidden');
    document.body.style.overflow = 'hidden';
}

function closeSlide() {
    document.getElementById('modal').classList.add('hidden');
    document.body.style.overflow = 'auto';
}
